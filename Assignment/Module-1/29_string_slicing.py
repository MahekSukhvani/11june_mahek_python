text = "Hello Python"

print("Original String : ", text)

print("Slice [0:5] : ", text[0:5])  

print("Slice [6:] : ", text[6:])       

print("Slice [:5] : ", text[:5])       

print("Slice [:] : ", text[:])        

print("Slice [::2] : ", text[::2])      

print("Reverse [: : -1] : ", text[::-1])   